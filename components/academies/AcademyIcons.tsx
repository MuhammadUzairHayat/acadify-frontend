export function StarRatingDisplay({
  rating,
  reviewCount,
  className = "",
}: {
  rating: number;
  reviewCount: number;
  className?: string;
}) {
  if (reviewCount === 0) {
    return <span className={`text-sm text-text-tertiary ${className}`}>No reviews yet</span>;
  }
  return (
    <span className={`inline-flex items-center gap-1 text-sm text-text-secondary ${className}`}>
      <IconStar className="h-4 w-4 text-yellow-400" filled />
      <span className="font-medium text-text-primary">{rating.toFixed(1)}</span>
      <span>({reviewCount})</span>
    </span>
  );
}
